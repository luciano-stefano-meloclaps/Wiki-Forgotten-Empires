CREATE TABLE IF NOT EXISTS "Ages" (
    "Id" INTEGER NOT NULL CONSTRAINT "PK_Ages" PRIMARY KEY AUTOINCREMENT,
    "Date" TEXT NULL,
    "Name" TEXT NOT NULL,
    "Overview" TEXT NULL,
    "Summary" TEXT NULL
, "BattleId" INTEGER NULL, "CharacterId" INTEGER NULL);
CREATE TABLE IF NOT EXISTS "Battles" (
    "Id" INTEGER NOT NULL CONSTRAINT "PK_Battles" PRIMARY KEY AUTOINCREMENT,
    "AgeId" INTEGER NULL,
    "Date" TEXT NULL,
    "DetailedDescription" TEXT NULL,
    "Name" TEXT NOT NULL,
    "Summary" TEXT NULL,
    "Territory" INTEGER NULL,
    CONSTRAINT "FK_Battles_Ages_AgeId" FOREIGN KEY ("AgeId") REFERENCES "Ages" ("Id") ON DELETE RESTRICT
);
CREATE TABLE IF NOT EXISTS "CharacterBattles" (
    "CharacterId" INTEGER NOT NULL,
    "BattleId" INTEGER NOT NULL,
    "Outcome" INTEGER NOT NULL,
    CONSTRAINT "PK_CharacterBattles" PRIMARY KEY ("CharacterId", "BattleId"),
    CONSTRAINT "FK_CharacterBattles_Battles_BattleId" FOREIGN KEY ("BattleId") REFERENCES "Battles" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_CharacterBattles_Characters_CharacterId" FOREIGN KEY ("CharacterId") REFERENCES "Characters" ("Id") ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS "Characters" (
    "Id" INTEGER NOT NULL CONSTRAINT "PK_Characters" PRIMARY KEY AUTOINCREMENT,
    "AgeId" INTEGER NULL,
    "CivilizationId" INTEGER NULL,
    "Description" TEXT NULL,
    "Dynasty" TEXT NULL,
    "HonorificTitle" TEXT NULL,
    "ImageUrl" TEXT NULL,
    "LifePeriod" TEXT NULL,
    "Name" TEXT NOT NULL,
    "Role" INTEGER NULL,
    CONSTRAINT "FK_Characters_Ages_AgeId" FOREIGN KEY ("AgeId") REFERENCES "Ages" ("Id") ON DELETE RESTRICT,
    CONSTRAINT "FK_Characters_Civilizations_CivilizationId" FOREIGN KEY ("CivilizationId") REFERENCES "Civilizations" ("Id") ON DELETE RESTRICT
);
CREATE TABLE IF NOT EXISTS "CivilizationAges" (
    "CivilizationId" INTEGER NOT NULL,
    "AgeId" INTEGER NOT NULL,
    CONSTRAINT "PK_CivilizationAges" PRIMARY KEY ("CivilizationId", "AgeId"),
    CONSTRAINT "FK_CivilizationAges_Ages_AgeId" FOREIGN KEY ("AgeId") REFERENCES "Ages" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_CivilizationAges_Civilizations_CivilizationId" FOREIGN KEY ("CivilizationId") REFERENCES "Civilizations" ("Id") ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS "CivilizationBattles" (
    "CivilizationId" INTEGER NOT NULL,
    "BattleId" INTEGER NOT NULL,
    CONSTRAINT "PK_CivilizationBattles" PRIMARY KEY ("CivilizationId", "BattleId"),
    CONSTRAINT "FK_CivilizationBattles_Battles_BattleId" FOREIGN KEY ("BattleId") REFERENCES "Battles" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_CivilizationBattles_Civilizations_CivilizationId" FOREIGN KEY ("CivilizationId") REFERENCES "Civilizations" ("Id") ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS "Civilizations" (
    "Id" INTEGER NOT NULL CONSTRAINT "PK_Civilizations" PRIMARY KEY AUTOINCREMENT,
    "ImageUrl" TEXT NULL,
    "Name" TEXT NOT NULL,
    "Overview" TEXT NULL,
    "State" INTEGER NOT NULL,
    "Summary" TEXT NULL,
    "Territory" INTEGER NOT NULL
);
CREATE TABLE IF NOT EXISTS "__EFMigrationsHistory" (
    "MigrationId" TEXT NOT NULL CONSTRAINT "PK___EFMigrationsHistory" PRIMARY KEY,
    "ProductVersion" TEXT NOT NULL
);
CREATE TABLE IF NOT EXISTS "__EFMigrationsLock" (
    "Id" INTEGER NOT NULL CONSTRAINT "PK___EFMigrationsLock" PRIMARY KEY,
    "Timestamp" TEXT NOT NULL
);
